<h1>Elasticity</h1>
<p>Elasticity is simply the measurement of how much something changes when something else changes.</p>
<p>In everyday life we can think of any number of things that will change based on something else. For example:</p>
<ul>
<li>If the temperature outside gets colder, how will this change the way I dress?</li>
<li>If I push on the accelerator, how will this change the speed of my car?</li>
<li>If I pull back on a rubber band and let it go, how far will it fly across the room?</li>
</ul>
<p>These events describe <em>relationships between variables</em>. And that&rsquo;s just what elasticity is.</p>
<p><strong>Elasticity</strong> is the <em>responsiveness of one variable to a change in another variable.</em></p>
<p>In economics we are concerned with the following variable relationships:</p>
<ul>
<li>If I change the <strong>price</strong> of my product, how will<strong> quantity demanded</strong> be affected?</li>
<li>If I change consumer <strong>income</strong>, how will<strong> quantity demanded</strong> be affected?</li>
<li>If the price of <strong>another product</strong> changes, how will this affect the <strong>quantity demanded</strong> for my product?</li>
<li>If the <strong>price</strong> of my product changes, how will this affect <strong>quantity supplied</strong>?</li>
</ul>
<h1>Price Elasticity of Demand (PED)</h1>
<p><strong>Price Elasticity of Demand</strong> (PED) is the responsiveness of <strong>quantity demanded</strong> following a <strong>change in price</strong>.</p>
<p>In other words: if we change our price, what will happen to our quantity demanded?</p>
<p>The idea of Price Elasticity of Demand has been implicit ever since we&rsquo;ve been talking about the demand curve. We know that if we change our price, we expect quantity demanded to change.</p>
<p>Price Elasticity of Demand simply gives us a way to measure this. We can use this measurement to predict what will happen following a price change and explain the implications for a business&rsquo; revenue.</p>
<h1>Price Elastic Demand and Price Inelastic Demand</h1>

<img class = "notesimg" src="files/124.2.JPG">
<p>To describe how price affect quantity demanded, we use two terms: <strong>price elastic</strong> and <strong>price inelastic.</strong></p>


<p>If demand is <strong>price elastic</strong> it means that Quantity Demanded changes <em>more than proportionally</em> following a change in price. This means that small changes in price will have large effects on our quantity demanded.</p>
<img class = "notesimg" src="files/124.3.JPG">
<p>If demand is <strong>price inelastic, </strong>it means that Quantity Demanded changes <em>less than proportionally</em> following a change in price. This means that large changes in price will have small effects on our quantity demanded.</p>
<p>Note the language that is used to describe elasticity: <em>more than proportional</em> and <em>less than proportional</em>. This is very important because when measuring changes in price and quantity we are not interested in raw changes. Instead, we are interested in changes relative to the original quantity. To do this we will always compare price and quantity changes using percentages. Because we are using percentages, we can always say that changes are <em>proportional</em>.</p>
<p>Take the following example: say that we have two products, Product A and Product B. Product A sells 5 units per day, and Product B sells 100 units per day. We lower the price on both, and each sees daily sales increase by 1 unit per day.</p>
<p>They have both increased by the same amount- but Product A has seen a much larger <em>proportional change</em>. This is because, relative to the original sales of 5 units per day, an increase of 1 is fairly large (20% of the original). Product B also increases sales by 1 unit, but relative to 100 units per day, an increase of 1 is tiny (only 1% of the original). So even though they have both increased by the same amount, the <em>proportional change</em> is much different.</p>
<p><a href="#~">Link: Proportionality Warm Up Quesitons</a></p>
<p>It is always a good idea to use the idea of <em>proportionality</em> when describing how elastic something is.</p>
<h1>The Sign of PED</h1>
<img class = "" src="files/124.1.JPG">
<p>Learning about the demand curve tells us the overall expected relationship between Price and Quantity Demanded: as Price goes up, Quantity Demanded goes down. The relationship is <strong>negative</strong>. This is the basis of the <strong>law of demand</strong> and is the reason why the demand curve slopes downwards.</p>
<h1>Formula for PED</h1>
