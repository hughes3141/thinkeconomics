<p>The number that this formula generates describes how responsive, mathematically, your quantity demand will be to a change in price. So if your PED is -2, and you change your price by 10%, you can predict that your Quantity Demanded will change by:</p>
<p>2 x 10% = -20%</p>
<p>Notice how the formula uses percentage change for Quantity Demanded and Price. Using this, rather than raw figures, gives the <em>proportional changes</em> that we discussed earlier.</p>
