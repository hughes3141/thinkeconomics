<h1>PED Coefficient and Elasticity</h1>
<p>Say you have the following information:</p>
<img class = "notesimg" src="files/124.6.JPG">
<ol>
<li>The price of rice increases by 10%.&nbsp; This has the effect of decreasing the quantity demanded for rice by 5%.&nbsp;</li>
</ol>
<p>Is this product elastic or inelastic? And what will its PED coefficient be?</p>
<p>First, we can see that this product is price <strong>inelastic</strong>. We can tell by looking at the numbers. The price has changed by 10%, and this has only made a difference of 5% for the quantity demanded. We can see that price affects quantity demanded <em>less than proportionally</em>, so this product is <strong>inelastic.</strong></p>
<p>To work out the PED coefficient we plug the numbers into the formula. Because percentage change in price is greater than the percentage change in Qd, we can see that this fraction is &lsquo;bottom heavy.&rsquo; A &lsquo;bottom heavy&rsquo; fraction will produce a decimal which is less than 1, which we can see in this answer: PED = -0.5.</p>
<p>Now consider this problem:</p>
<img class = "notesimg" src="files/124.7.JPG">
<ol start="2">
<li>The price of oranges decreases by 5%.&nbsp; This has the effect of increasing the quantity demanded by 7.5%.&nbsp;</li>
</ol>
<p>Is this product elastic or inelastic? And what will its PED coefficient be?</p>
<p>First, we can see that this product is price <strong>elastic. </strong>The numbers show that quantity demanded increases by a greater proportional amount than price. This produces a &lsquo;top heavy&rsquo; fraction, which will produce a decimalised answer which is greater than 1: PED = -1.5.</p>




