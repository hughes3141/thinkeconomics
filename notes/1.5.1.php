<h1>Marginal, Average, and Total</h1>
<p>Say that you&rsquo;re looking at all the costs that a business needs to pay. When analysing the costs, you could think of them in the following three ways:</p>
<p><strong>Total</strong>: Add all the <u>(costs)</u> together.</p>
<p><strong>Average</strong>: Add all the <u>(costs)</u> and divide by the number of units sold.</p>
<p><strong>Marginal</strong>: The additional <u>(costs)</u> that I get from producing <em>one extra unit</em>.</p>
<p>Note that we could substitute <u>(costs)</u> for other variables (e.g. revenue, output, utility) when discussing these concepts.</p>
<p>There is a relationship here:</p>
<ul>
<li>To work out the <strong>Marginal</strong>, work out the difference between the <strong>Total</strong> of the previous unit value and the <strong>Total </strong>this unit value.</li>
<li>To work out the <strong>Total</strong>, add on the <strong>Marginal</strong> to the <strong>Total</strong> of the previous unit value.</li>
</ul>
