<h1>Relationship Between Marginal and Average</h1>
<img src="files/151.2.jpg" alt="Graph of Flipping a coin"   style="float:right;border:1px solid black; max-width:50%">
<p>Take the example chart of a person flipping a coin. This person is pretty streaky with their flipping: several wins (marginal rate = 1) in a row, then several losses (marginal rate = 0), followed by several wins. What happens to the average and marginal figures?</p>

<p>At first the average and marginal are the same, and as long as the person keeps winning they remain at 1. But look what happens when the person starts the first losing streak. Marginal jumps to 0 immediately, and average follows more slowly. As long as the person keeps losing, average keeps falling.</p>
<p>Then look what happens when they start winning again. Marginal jumps to 1 immediately, and average starts to rise. Notice how marginal crosses average at a minimum point for average. As long as the streak continues and marginal = 1, average will continue to climb.</p>
<p>Later streaks of winning ad losing follow the same pattern. But as the number of rounds increases, the average rises and falls ever more slowly. It&rsquo;s as though the average is weighed down by all the rounds that have come before, and by the time it gets to higher rounds it&rsquo;s &lsquo;heavy&rsquo; with its own history. Yet the marginal has no such worries; it is free to flip as quickly as it pleases, as it is only concerned with <em>the value of the next round</em>.</p>
<p>Given that average is the <em>average of this round and all previous rounds</em>, and the marginal is the value of <em>this next round only</em>, this makes perfect sense.</p>
<h1>Batting Averages and Batting Marginals</h1>
<img src="files/151.3.jpg" alt="Nolan Arenado Batting Average 2019"  style="float:right;border:1px solid black; max-width:50%; margin: 10px;">
<p>Another way of illustrating this is through baseball. Baseball uses a statistic called a <em>batting average. </em>This measure is a percentage of the number of times a player is <em>at bat</em> (i.e. has a go) where they <em>get a hit </em>(i.e. advance to a base). This figure is calculated per game, per season, and over a batter&rsquo;s career. Some games a batter will try a few times and never get to base and the batting average (for the game) would be 0. Some games a player will get a hit every time they are at bat and their batting average (for the game) would be 1. A professional player with a batting average above 0.300 for the season is considered very good.</p>
<p>The chart shows Nolan Arenado of the Colorado Rockies&rsquo; batting performance for the 2019 season. The blue line is the batting average per game; let&rsquo;s say this is the <em>marginal</em>. The orange line is the batting average for the season; let&rsquo;s say this is the <em>average</em>.</p>
<p>The chart shows what we would expect to see: orange <em>average</em> follows the blue <em>marginal</em>, and in the early games the <em>average</em> can travel quite freely. At about 50 games Nolan has a streak of great games, which brings his season <em>average</em> up to nearly 0.350 for that part in the season. As the season goes on the <em>average</em> levels out; the good games still bring up the season <em>average</em>, but not as much as they did in the early part of the season. Nolan finishes the season with a great streak of games, but by this point the <em>average </em>is pretty set so it only changes slightly.</p>
<p>It&rsquo;s as we said before: marginal is free and average is heavy, and average always follows the marginal.</p>
