<h1>What is a market structure?</h1>
<p>Think about two products that you have purchased recently. What made them different? Better yet, what was it about the types of businesses where you bought them that made them different?</p>
<p>In Economics we refer to the conditions of the market as a <strong>market structure</strong>. It describes the assumptions that we make when describing the business conditions, which informs us of the output that we will see as consumers.</p>
<p>A market structure will depend on the following:</p>
<ul>
<li><strong>Number of firms: </strong>How many firms are there in the market? This may link strongly to barriers to entry.</li>
<li><strong>Information available to customers and businesses: </strong>Do customers have the full picture of what is happening in the market? As an example, do they have access to different prices charged by different businesses?</li>
<li><strong>How easy can firms enter/exit the market: </strong>These are referred to as <strong>barriers to entry</strong> and <strong>barriers to exit. </strong>They describe how easily new businesses can enter the market, or how easily existing businesses can leave the market.</li>
<li><strong>Product differentiation: </strong>Describes the type of product being sold and whether competitors sell products that are different from each other.</li>
</ul>
<h1>Barriers to Entry</h1>
<p>A <strong>barrier to entry</strong> is a start-up cost or other obstacle that prevents new competitors from easily entering an industry or area of business. <a target ="_blank" href="https://www.investopedia.com/terms/b/barrierstoentry.asp">Source: Investopedia</a></p>
<p>Barriers to entry benefit existing firms because they protect their revenues and profits.</p>
<p>Barriers to entry take two forms: <strong>Structural Barriers </strong>and <strong>Behavioural Barriers.</strong></p>
<h1>Structural Barriers to Entry</h1>
<p>These barriers to entry describe the natural state of the market. These are basic industry conditions that exist because of the nature of the market- e.g. how necessary is it to produce at scale? Or does this type of product lend itself to network effects? <a target ="_blank" href="https://www.oecd.org/competition/mergers/37921908.pdf#targetText=Conditions%20that%20constitute%20entry%20barriers,of%20scale%20and%20network%20effects.">More info.</a></p>
<p>Example include:</p>
<ul>
<li><strong>Economies of scale</strong>. These are there aspects of this industry that allow large firms to push down their unit costs? In other words: are there factors that make it unprofitable to enter this industry if you are not a large business?
<ul>
<li>Example: IMERYS in Cornwall is big enough to be able to purchase machinery, equipment, and specialist managers that enable it to extract kaolinite (china clay) for a low unit cost. It would be difficult to enter this market because a small business would not have these advantages.</li>
</ul>
</li>
<li><strong>Network effects</strong>. This describes when a business is more valuable to an individual user because of the size of their <em>network</em>. A new entrant without an established network would find it difficult to compete with those established businesses who have existing networks. (Also referred to as <em>networking externalities</em>)
<ul>
<li>Example: Instagram is valuable to any individual user because that user&rsquo;s friends are all on it. It would be hard to make a new social network because you&rsquo;d have to get enough people on it before it is useful to anybody else.</li>
</ul>
</li>
<li><strong>Control of key resources</strong>. Sometimes one business will be the only one to control a natural resource.
<ul>
<li>Example: DeBeers has a monopoly on diamond mining because it controls most of the world&rsquo;s diamond production in South Africa.</li>
</ul>
</li>
<li><strong>High start-up costs</strong>. Some types of businesses need a lot of investment before they are viable. Smaller firms without the ability to pay these initial costs will not be able to enter the market.</li>
<li><strong>High R&amp;D costs</strong>. Some processes require the business to spend money innovating the product before they can bring it to market. Additionally, the <em>patent</em> granted to incumbent firms following the Research and Development process acts as a barrier to entry because new firms cannot copy the product.
<ul>
<li>Example: Pharmaceutical companies spend billions on new products. Furthermore, once they have developed a new drug it cannot be copied.</li>
</ul>
</li>
<li><strong>Advertising</strong>. A type of economy of scale (marketing economies of scale). In certain sectors incumbent firms will spend heavily on advertising, making it more difficult for new firms to break through.</li>
<li><strong>Legal barriers</strong>. In certain sectors, a business will need to have legal permission before it can begin trading. Restriction of this permission by governments limit the number of firms that can enter the industry.
<ul>
<li>Example: In NYC pre-Uber there were tight regulations on who could drive a &lsquo;yellow taxi&rsquo; cab. Without this legal permission (known as a &lsquo;medallion&rsquo;) a new firm could not compete. This protected those drivers with &lsquo;medallions.&rsquo;</li>
<li>Example: Many legal barriers exist in labour markets. Many professions are &lsquo;protected&rsquo; by qualifications, i.e. if you don&rsquo;t have the qualification you can&rsquo;t compete (e.g. qualification to be a florist in certain US states). This makes it harder to enter the profession, thereby allowing those in it to earn higher wages.</li>
</ul>
</li>
</ul>
<h1>Behavioural Barriers to Entry</h1>
<p>These barriers to entry are enacted deliberately by incumbent firms from entering the market. They do not exist because of natural market conditions; they are artificially created by incumbent firms to protect themselves from competition. <a target ="_blank" href="https://www.oecd.org/competition/mergers/37921908.pdf#targetText=Conditions%20that%20constitute%20entry%20barriers,of%20scale%20and%20network%20effects.">More info.</a></p>
<p>Examples include:</p>
<ul>
<li><strong>Limit Pricing. </strong>Limit pricing occurs when a firm, rather than profit maximising in the short run, sets a price low enough to deter new entrants from entering the market.
<ul>
<li>In other words: <em>I</em> could set a higher price and make more profit. However, I decide to set a price lower than this high price. This is because the lower price keeps <em>you</em> from joining the market (maybe you can&rsquo;t make a profit at this price).</li>
<li>More info: <a target ="_blank" href="https://www.economicshelp.org/blog/glossary/limit-pricing/">org</a>, <a target ="_blank" href="https://en.wikipedia.org/wiki/Limit_price">Wikipedia</a>, <a target ="_blank" href="https://stats.oecd.org/glossary/detail.asp?ID=3246">OECD</a>.</li>
</ul>
</li>
<li><strong>Vertical integration. </strong>Vertical integration refers to a single firm merging with other firms before or after its place in the supply chain. Example: Ford Motor company (which produces cars) could purchase its suppliers, the tyre manufacturers and rubber producers (vertical backward integration). It could also purchase its distributors, the dealerships that sell cars (vertical forward integration). Having control of suppliers or distributors means the firm can limit or prohibit the availability of these services to its competitors, creating a barrier to entry</li>
<li><strong>Switching costs. </strong>These are costs that firms charge their customers in the event that they change suppliers. Example: phone companies often have 18-month contracts with penalties for customers who cancel early. If it is difficult for the customers of an incumbent firm to leave their service, it will act as a barrier to entry for new firms.</li>
<li><strong>Exclusive contracts and licensing. </strong>An exclusive contract means that only one firm will be allowed to supply a particular product. Licensing means that permission must be granted before using or producing a product. These strategies prevent new firms from having access to the latest products and technology.
<ul>
<li>Example: when Apple iPhone was released in 2007 in the UK, O2 had an exclusive deal to be the only network to carry it.</li>
</ul>
</li>
<li><strong>Loyalty schemes: </strong>These schemes reward customers for loyalty and make them less likely to switch suppliers, making it more difficult for new firms to enter the market.</li>
</ul>
<h1>Contestability</h1>
<p>Contestability refers to the degree to which other firms can freely enter and exit a market.</p>
<p>A market is said to be <em>perfectly contestable</em> if the following conditions apply:</p>
<ul>
<li>No barriers to entry and exit</li>
<li>No sunk costs</li>
<li>All firms have access to the same technology</li>
<li>All firms are subject to the same regulations</li>
<li>Low consumer loyalty</li>
</ul>
<p><strong>Barriers to exit</strong> refer to those obstacles that prevent firms from leaving the market. More info: <a target ="_blank" href="https://www.investopedia.com/terms/b/barriers-to-exit.asp#:~:text=Key%20Takeaways-,Barriers%20to%20exit%20are%20obstacles%20or%20impediments%20that%20prevent%20a,write%2Doffs%20and%20closure%20costs">Investopedia</a>.</p>
<p><strong>Sunk costs</strong> refer to costs which are paid by a firm and cannot be reclaimed if the firms decides to leave the market. They are a type of a barrier to exit.</p>
<p>Examples of sunk costs:</p>
<ul>
<li>Poker (Texas hold &rsquo;em): Players to the left of the dealer have to put up money (called &ldquo;small blind&rdquo; and &ldquo;big blind&rdquo;) before they are even dealt their cards. If they get their cards and decide they want to leave the round (fold), they can&rsquo;t get their money back. The blinds are <strong>sunk cost</strong>.</li>
<li>Say you pay tickets for a concert and see the first song. You realise you don&rsquo;t like the band and decide to leave. You can&rsquo;t get a refund for your ticket; it is a <strong>sunk cost</strong>.</li>
</ul>
<p>Contestability is all about the ease with which firms can enter and exit a market. <em>It has nothing to do with the number of firms in the market</em>.</p>
<p>There is an interesting conclusion that comes with contestable market theory: you may have a&nbsp; market that achieves allocative efficiency, even though there may be only one firm in the market. Here&rsquo;s how:</p>
<ul>
<li>A market may be said to be contestable if it meets the criteria set out above. Say, for the sake of argument, there is only one firm in this market</li>
<li>This firm could decide to charge monopoly prices (higher than allocative efficiency prices). However, because the market is <em>contestable </em>(i.e. other firms can enter the market freely), high prices would encourage <strong>hit and run competition</strong>. New competitors would &lsquo;swoop up&rsquo; any excess profits made by the incumbent firms, then leave again by the time incumbent firms can put their prices down.</li>
<li>In order to prevent this, incumbent firms therefore keep prices low to deter outside firms from entering.</li>
</ul>
