<p>You will recall that this is all very similar to the sorts of changes that will affect a Production Possibility Frontier (PPF). In fact, LRAS and a PPF both describe <em>the same thing</em>.</p>
<img src="files/215_3.jpg" class="img-fluid mx-auto d-block" alt="LRAS and PPF">
<p>A <em>right</em> shift in LRAS can be interpreted as shifting <em>out</em> the PPF.</p>
<p>A <em>left</em> shift in LRAS can be interpreting as shifting <em>in </em>the PPF.</p>
<p>There is also a link between LRAS and the business cycle. LRAS represents long run or potential growth. As LRAS keeps shifting to the right, the trend rate of growth keeps increasing:</p>
<img src="files/215_4.jpg" class="img-fluid mx-auto d-block" alt="LRAS and Business Cycle">
<p>This is because both LRAS and the trend rate of growth are affected only by <em>real</em> factors in the economy: the underlying conditions that determine the quality and quantity of factors of production.</p>
<h2>Short Run Aggregate Supply (SRAS)</h2>
<p><strong>Short Run Aggregate Supply</strong> <strong>(SRAS) </strong>is the amount that an economy can produce at any given price level.</p>
<p>In the Short Run we assume</p>
<ul>
<li>nominal wage rates are fixed</li>
<li>at least one factor of production (usually capital) is fixed.</li>
</ul>
<p>In other words: we can make more, but only with the capital we&rsquo;ve got currently. And our workers can&rsquo;t negotiate a new wage level (yet).</p>
<p>The first assumption that nominal wages are fixed is a technical one. It means that for any given SRAS curve, we assume that we cannot change the money wages we pay to workers. When we change the wages of workers, we need a new SRAS curve (as we&rsquo;ll see in the next section).</p>
<p>The second assumption is pretty standard assumption of the short-run, which is more well-used when looking at Theory of the Firm.</p>
<img src="files/215_6.jpg" class="img-fluid mx-auto d-block" alt="The SRAS Curve">
<p>The SRAS curve slopes upwards because a higher price level in the economy incentives firms to increase their output. The higher prices will allow them to make greater profits.</p>
<p>SRAS shifts due to <em>nominal</em> changes in the economy, meaning changes to the costs in factors of production. This does not affect the quality or quantity of factors of production, but rather the price that is paid for them.</p>
<p>Any factor which will make your factors of production <em>cheaper</em> will shift SRAS to the right. For any given price level, firms will be able to produce <em>more</em>.</p>
<p>Any factor which will make your factors of production <em>more expensive</em> will shift SRAS to the left. For any given price level, firms will be able to produce <em>less</em>.</p>
