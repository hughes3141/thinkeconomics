<h2>Difference between Keynesian and Neoclassical Models</h2>
<p>The key difference between the Keynesian and Neoclassical AD/AS models is:</p>
<ul>
<li>The Keynesian model assumes that markets will not clear, due to <strong>sticky wages</strong>. <em>Implication: get the government involved to start up the Economy.</em></li>
<li>The Neoclassical model assumes that markets will clear (eventually). <em>Implication: leave the market alone, it will sort itself out.</em></li>
</ul>
<h2>Long Run Aggregate Supply (LRAS)</h2>
<p>This model looks at the economy over two time periods, and therefore has two different types of Aggregate Supply curves:</p>
<ul>
<li>Long Run Aggregate Supply (LRAS)</li>
<li>Short Run Aggregate Supply (SRAS)</li>
</ul>
<img src="files/215_1.jpg" class="img-fluid mx-auto d-block" alt="LRAS Curve">
<p><strong>Long Run Aggregate Supply (LRAS) </strong>shows the total output that the economy can produce. It represents the total possible output from our factors of production.</p>
<p>It is <em>perfectly inelastic</em> because production levels in the long run do not depend on price. They only depend on the quality and quantity of factors of production.</p>
<p>The LRAS while shift due to <em>real changes</em> in the economy. This means that we have made changes in the quality and quantity of factors of production. These are changes that are out in the real world and affect our ability to make things, rather than just changes that affect the price of things.</p>

