<h1>A Level Economics Course Guide</h1> <p>Quick Guide for A Level Economics Students</p>
<h2>Classroom Expectations</h2>
<p>Respect</p>
<ul>
<li>Respect for your college environment</li>
<li>Respect for others</li>
<li>Respect for yourself</li>
</ul>
<p>Bring a well-charged mobile device (laptop, tablet, or phone) to lessons to access learning resources. Do not allow this device to distract from your learning.</p>
<p>Water is allowed in the classroom; other food and drink are not during lesson times.</p>
<h2>Attendance and Punctuality</h2>
<p>100% attendance is expected. Consistently poor attendance or punctuality will result in discussion with your tutor.</p>
<p>If you are unexpectedly late you should: come to the lesson, register at the swipe box, and sit down without interrupting. Explain the circumstances later.</p>
<h2>Classroom Materials</h2>
<p>For this course you should have:</p>
<ul>
<li>A notebook with lined paper</li>
<li>Pencil, pen, and ruler. Multicoloured pens are highly encouraged.</li>
<li>A calculator (better if not on phone)</li>
<li>A place to keep returned work (e.g. lever arch file or ring binder)</li>
</ul>
<p>You will be provided with an Economics textbook for your use throughout the A Level course. You do not need to bring this to lessons.</p>
<h2>Learning A Level Economics</h2>
<p>Some common approaches that work well for learning the subject:</p>
<ul>
<li>Keep a rough notebook for first drafts and initial ideas. Draw a graph, then draw it again. Keep practising it until you can do it quickly and with no mistakes. <strong>And don&rsquo;t be afraid to make mistakes.</strong></li>
<li>Read ahead in your textbook. Read the BBC economics news and <em>The Economist</em>. Read blogs and websites that teach the material.</li>
<li>Keep a neat notebook where you consolidate your learning to strengthen the connections in your brain.</li>
<li>Do your assigned work, turn it in on time.</li>
<li><strong>Learn from your mistakes</strong>. Making mistakes is an essential part of learning.</li>
</ul>
<h2>Independent Learning and Personal Initiative</h2>
<ul>
<li>Use the year plan opposite to find out what we will be learning, then read ahead in your textbook.</li>
<li>Make a habit of keeping a look out in the news for stories that are related to what we are studying. Keep a folder of favourite pages dedicated to interesting Economics news. Email your teacher if you think an article is interesting.</li>
<li>Read <em>The Economist </em>through the college. Read the BBC Economy news page.</li>
<li>Ask for help from your teacher if you have any additional questions. Come to weekly catch-up sessions if you want to go over work.</li>
<li>&lsquo;Lean in&rsquo; and be enthusiastic. Always try your best, even when things seem difficult. Ask when you have a question. Allow yourself to take full advantage of the course. Have fun and enjoy the subject.</li>
</ul>
