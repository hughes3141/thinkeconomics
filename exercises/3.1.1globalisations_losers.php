<p><strong>Short summary</strong>: This article explores the reasons for why globalisation has increased regional inequality in places like the USA, and the policies that can be done to help correct this.</p>
<p><strong>Longer summary: </strong>(P = paragraph no.)</p>
<ul>
<li>Political parties that are anti-globalisation are on the rise, despite their policies being harmful to their supporters. (P1-2)</li>
<li>Globalisation means that some cities get incredibly rich (e.g. San Francisco, London) while others become very poor (e.g. Blackpool, Detroit). Economies of scale mean that globalised firms cluster in &lsquo;superstar places.&rsquo; (P3-4)</li>
<li>Despite the wider income between places, people are less likely to move. Big cities are too expensive (because not enough houses are built). Dual-income families depend on two jobs and are less mobile. People are now looking after their parents and can&rsquo;t move away from them. Welfare payments make people less likely to move from struggling places. (P5-6)</li>
<li>The way to solve this problem is to:
<ul>
<li>Build more houses in thriving places</li>
<li>Remove occupational licensing for workers.</li>
<li>Subsidies places that have been left behind.</li>
<li>Improve competition policy to allow jobs to be created outside of big cities.</li>
<li>Improve the provision of local colleges and training centres.</li>
<li>Encourage big firms (e.g. Amazon) to build headquarters in other cities besides the big ones. (P7-9)</li>
</ul>
</li>
</ul>
