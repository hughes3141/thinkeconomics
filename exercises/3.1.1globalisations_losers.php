<p>21 October 2017<br/> source: <a href="https://www.economist.com/leaders/2017/10/21/the-right-way-to-help-declining-places" target="_blank">https://www.economist.com/leaders/2017/10/21/the-right-way-to-help-declining-places</a></p>
