<li><strong>Event:</strong> &ldquo;Paddle boards are everywhere these days.&rdquo; <strong>Market</strong>: Paddle Boards.</li>
<a href="#/" onclick="id01()">Click for answer</a>
<div class ="ans" id="id01"><img src="files/121_right.jpg" alt="Demand Curve Shifts Right" width="200" height="200">
<p>Paddle boards seem to have grown in popularity. This suggests that <strong>tastes and fashion</strong> have shifted the demand for this product to the <strong>right.</strong></p>
</div>

<li><strong>Event:</strong> Boardmasters Festival cuts prices. <strong>Market</strong>: Train tickets to Newquay from up country.</li>
<a href="#/" onclick="id02()">Click for answer</a>
<div class ="ans" id="id02"><img src="files/121_right.jpg" alt="Demand Curve Shifts Right" width="200" height="200">
<p>When Boardmaster festival cuts its prices then more people from upcountry will want to attend the festival. Many will travel via train to get there. A train ticket to Newquay is a <strong>complementary good</strong> to a festival ticket. Lower prices for the festival will increase the demand for these complementary goods.</p>
</div>

<li><strong>Event:</strong> Amazon cuts price on Samsung Galaxy Phones from &pound;800 to &pound;400 on cyber-Monday. <strong>Market</strong>: Samsung Galaxy Phones.</li>
<a href="#/" onclick="id03()">Click for answer</a>
<div class ="ans" id="id03"><img src="files/121_ext.jpg" alt="Extension in Demand" width="200" height="200">
<p>When Amazon cuts the price for the phone there will be an <strong>extension in demand</strong>. The demand function hasn&rsquo;t changed; quantity demanded will increase because of the lower price.</p>
</div>

<li><strong>Event:</strong> Boardmasters festival is cancelled at the <a href ="https://www.bbc.co.uk/news/uk-england-cornwall-49259812" target ="|blank">last minute</a>. <strong>Market</strong>: Camping sites in Newquay.</li>
<a href="#/" onclick="id04()">Click for answer</a>
<div class ="ans" id="id04"><img src="files/121_left.jpg" alt="Demand Curve Shifts Left" width="200" height="200">
<p>When Boardmasters festival was cancelled at the last minute in 2019, many people who were planning to come to Newquay cancelled their trip at the last minute. This meant that the campsites in Newquay who were expecting them suddenly felt a shift <strong>left</strong> in demand.</p>
<p><em>Note: An alternative explanation for this question is that there were people who had already travelled to Newquay for the festival, and when it was cancelled they suddenly needed a new place to camp. So the demand for local campsites would shift <strong>right. </strong>This makes sense. In reality, however, this was offset by the number of people who cancelled their trips altogether.</em></p>
</div>

<li><strong>Event:</strong> Thomas Cook Holiday firm (which includes airlines) goes bankrupt. <strong>Market</strong>: Competitor airlines that run similar routes.</li>
<a href="#/" onclick="id05()">Click for answer</a>
<div class ="ans" id="id05"><img src="files/121_right.jpg" alt="Demand Curve Shifts Right" width="200" height="200">
<p>When Thomas Cook went bankrupt, there were still people who wanted to go on holiday to these destinations. Some of them were already on holiday and needed a new airline to get home. Thomas Cook&rsquo;s departure would have been a decrease in the <strong>number of substitute goods</strong>. This would have shifted demand for these competitors to the <strong>right.</strong></p>
</div>

<li><strong>Event:</strong> Economic growth is strong. <strong>Market</strong>: Land Rovers.</li>
<a href="#/" onclick="id06()">Click for answer</a>
<div class ="ans" id="id06"><img src="files/121_right.jpg" alt="Demand Curve Shifts Right" width="200" height="200">
<p>Land Rovers are a <strong>normal good</strong>; demand for them increases as incomes increase. Strong economic growth means incomes are growing, so demand for Land Rovers shifts <strong>right.</strong></p>
</div>

<li><strong>Event</strong>: The UK government does not require vehicle tax on electric vehicles. <strong>Market</strong>: Electric vehicles.</li>
<a href="#/" onclick="id07()">Click for answer</a>
<div class ="ans" id="id07"><img src="files/121_right.jpg" alt="Demand Curve Shifts Right" width="200" height="200">
<p>Vehicle tax can be seen as a <strong>complementary good</strong> to vehicles. When the government puts tax at price = &pound;0 for electric cars, they are effectively decreasing the price of this complementary good. This would shift the demand for electric vehicles to the <strong>right.</strong></p>
</div>

<li><strong>Event</strong>: Cornwall Airport Newquay opens a new route to London Heathrow<strong>. Market</strong>: Hotel rooms in Newquay</li>
<a href="#/" onclick="id08()">Click for answer</a>
<div class ="ans" id="id08"><img src="files/121_right.jpg" alt="Demand Curve Shifts Right" width="200" height="200">
<p>Cornwall Airport gained a route to London Heathrow in March 2019. This makes it easier to travel from London to Newquay. With greater ease of this <strong>complementary good</strong> there will be greater demand for hotels in Newquay (e.g. easier to stay for the weekend). This will push demand for hotels in Newquay to the <strong>right.</strong></p>
</div>

<li><strong>Event</strong>: Camel Trail Cycle Hire has a cut-price sale on bikes for the weekend. <strong>Market</strong>: Bike hire in Wadebridge.</li>
<a href="#/" onclick="id09()">Click for answer</a>
<div class ="ans" id="id09"><img src="files/121_ext.jpg" alt="Extension in Demand" width="200" height="200">
<p>Camel Trail Cycle Hire is in the market for bike hire in Wadebridge. Cutting their price is an example of <strong>extension in demand. </strong>The demand function hasn&rsquo;t changed; quantity demanded will increase because of the lower price.</p>
</div>

<li><strong>Event: </strong>Camel Trail Cycle Hire has a cut-price sale on bikes for the weekend. <strong>Market</strong>: Restaurants in Padstow.</li>
<a href="#/" onclick="id10()">Click for answer</a>
<div class ="ans" id="id10"><img src="files/121_right.jpg" alt="Demand Curve Shifts Right" width="200" height="200">
<p>It&rsquo;s popular to hire a bicycle in Wadebridge and cycle to Padstow for lunch; these are <strong>complementary goods</strong>. When Camel Trail Cycle hire cuts its prices, this means that more people will want to cycle to Padstow for lunch, shifting the demand for restaurants in Padstow to the <strong>right.</strong></p>
</div>

<li><strong>Event: </strong>Lidl and Aldi have massive sales growth following the recession of 2008. <strong>Market:</strong> Aldi and Lidl.</li>
<a href="#/" onclick="id11()">Click for answer</a>
<div class ="ans" id="id11"><img src="files/121_right.jpg" alt="Demand Curve Shifts Right" width="200" height="200">
<p>Lidl and Aldi are, arguably, <strong>inferior goods </strong>(or they were seen to be in 2008/2009). A recession means that incomes are falling. As incomes fall, people buy more <strong>inferior goods. </strong>Demand for Aldi and Lidl shifts <strong>right.</strong></p>
</div>

<li><strong>Event</strong>: Bodmin promotes itself as a &lsquo;Cycling Town&rsquo; after &pound;7.5m investment to make roads more cycle friendly. <strong>Market</strong>: Cafes in Bodmin.</li>
<a href="https://www.cioslep.com/assets/file/Strategic%20Economic%20Plan/A3%20Sheets/A3%20Sheet%20-%20Bodmin%20Cornwall%20cycle%20town.pdf" target ="|blank"><img src="files/121_bodmin.jpg" alt="Bodmin Cycling Plan" width="400" height="100"><br></a>
<a href="#/" onclick="id12()">Click for answer</a>
<div class ="ans" id="id12"><img src="files/121_right.jpg" alt="Demand Curve Shifts Right" width="200" height="200">
<p>As Bodmin attracts more cyclists (e.g. those making a trip on the Camel trail from Wadebridge), more of these cyclists will stop in the local cafes. This is a <strong>change in tastes/fashion</strong>. This shifts demand for cafes in Bodmin to the <strong>right.</strong></p>
</div>

<li><strong>Event: </strong>Nansledan housing development is being built, adding 4,000 new houses just outside of Newquay town centre. <strong>Market</strong>: Houses in Newquay town centre.</li>
<img src="files/nansledan.jpg" alt="Nansledan Development in Newquay" width="450" height="250">
<br><a href="#/" onclick="id13()">Click for answer</a>
<div class ="ans" id="id13">
<img src="files/121_left.jpg" alt="Demand Curve Shifts Left" width="200" height="200">
<p>The Nansledan development means there is a greater number of substitute goods for people looking for housing in Newquay. This increase in the number of substitutes outside of town shifts demand for houses in town to the <strong>left.</strong></p>
</div>

<li>Challenge: <strong>Event: </strong>Pound sterling loses value against the US dollar. <strong>Market</strong>: Holidays to the USA.</li>
<a href="#/" onclick="id14()">Click for answer</a>
<div class ="ans" id="id14"><img src="files/121_left.jpg" alt="Demand Curve Shifts Left" width="200" height="200">
<p>When people go on holiday to the USA, exchanging their money from pounds to dollars is a type of <strong>complementary good</strong>. A weaker pound means that the price of dollars has gone up; the <strong>complementary good</strong> has increased in price. This will shift the demand for holidays to the USA to the <strong>left.</strong></p>
</div>
