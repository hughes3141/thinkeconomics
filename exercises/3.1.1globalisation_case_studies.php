<p>Jodhpur is a city in the state of Rajasthan in north-west India, a dry and desert-like part of the country. For centuries it has been home to skilled craftsmen who are known for their beautiful hand-carved wood items. Modern furniture manufacturers in Jodhpur continue this tradition and produce furniture that is famous for its quality throughout the world.</p>
