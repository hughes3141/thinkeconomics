<h3>Silicon Valley, California</h3>
<p>Silicon Valley is a region in California, USA, near San Francisco. It is home to many of the most successful technology companies in the world. Companies such as Google, Apple and Facebook were created or developed here, and their reach and customer base is now global.</p>
<p>Apple uses global marketing to sell millions of iPhones. In the first three months of 2017, the company sold 78.29 million iPhones. This is equivalent to 554 iPhones every minute. In 2017 Apple reported profit of $48.3bn. Software engineers working at Apple&rsquo;s headquarters in California earn on average $150,000 per year, compared to an annual household income of $59,000 nationally. San Francisco is now one of the most expensive places to live in the USA, with average house prices in the city of $1.61 million compared with $188,000 nationally.</p>
<h3>Flint, Michigan</h3>
