<p>In the 1990s India reformed its economy to be less state-planned and more market-orientated. One result of this was to allow furniture makers in Jodhpur to sell their exports across the world. Foreign interior-furnishing companies such as Laura Ashley (founded in the UK, Malaysian-owned) and Crate and Barrel (USA) made large orders from furniture-makers in Jodhpur. Skilled carpenters in India are able to make around 500 rupees ($7.50) per day, compared to a national average of 270 rupees ($4) per day. The national minimum wage in the USA for low skilled workers is $7.25 per hour. Consumers in Europe and America value the high quality of the products that are produced here. Furniture of similar quality made in Europe and America retails at a higher price than Indian-made furniture. By 2010, 90% of production of furniture manufactured in Jodhpur was exported abroad, mostly to Europe and America.</p>
