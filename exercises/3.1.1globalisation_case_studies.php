<p>Flint is a city in Michigan, USA. It was once a major centre of US automobile production. In the past 50 years American car manufacturers have moved their production facilities to countries with lower labour costs, such as South Korea, India, and China. In 1968 the city employed 80,000 workers in car factories in Flint; by 2011 there were only 6,000. The average household income in Flint is now $44,000 per year, compared to $59,000 nationally. In 2017, 45% of residents in Flint lived below the poverty line. In March 2018 the unemployment rate in Flint was 10.4%, compared with 4.1% nationally. In 2016 Donald Trump won the state of Michigan by 11,000 votes (of 4.8m votes cast), running on the promise of bringing back manufacturing jobs to America.</p>
