<h1>Exchange Rate Systems</h1>
<img src="files/234_05.JPG" style="float: right; width: 400px; height: auto; margin: 10px">
<p>In theory, exchange rates are either <strong>fixed </strong>or <strong>floating. </strong></p>
<p><strong>Fixed exchange rates</strong> imply that a country identifies another currency at which it will guarantee a specified exchange rate. It will then take direct and indirect measures to ensure that the market exchange rate equals the fixed exchange rate.</p>
<p><strong>Floating exchange rates</strong> imply that a country lets the market determine the exchange rate, and it does not intervene to determine some specified rate.</p>
<p><em>You should be familiar with the advantages and disadvantages of each system.</em></p>
<p>In reality, different countries have policies which are usually a mixture of each system. The different classifications are described below:</p>
