<h2>Monetary Policy</h2>
<p>Read the information <a href="https://www.bankofengland.co.uk/monetary-policy">on this page</a> and answer the following:</p>
<ol start="16">
<li>What is monetary policy?
<ol>
<li>Monetary policy is action that a country's central bank or government can take to influence how much money is in the economy and how much it costs to borrow.</li>
</ol>
</li>
<li>What are the two tools the Bank of England has to set monetary policy?
<ol>
<li>Setting the interest rate and quantitative easing.</li>
</ol>
</li>
<li>What are the objectives of monetary policy?
<ol>
<li>The primary objective is to keep inflation at 2%. Beyond that, they support the government&rsquo;s objectives of economic growth and employment.</li>
</ol>
</li>
<li>What are the current figures for the Bank Rate and the Inflation Rate?
<ol>
<li>(April 2021): Bank Rate: 0.1%. Inflation rate: 0.7%</li>
</ol>
</li>
<li>What is the Monetary Policy Committee and how do they decide what action to take?
<ol>
<li>The MCP has nine members. They meet eight times a year. They analyse the most recent economic data and then vote on how monetary policy should be set. This decision is agreed and then announced at 12noon on Thursday of that week.</li>
</ol>
</li>
<li>How long does it take for monetary policy to have its full effect on the economy?
<ol>
<li>Two years.</li>
</ol>
</li>
</ol>
