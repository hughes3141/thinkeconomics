<h1>Evaluation of Interest Rates</h1>
<p>You should be familiar with how interest rates can control inflation in the economy via the monetary policy transmission mechanism.</p>
<p>You should also be able to evaluate how effective interest rates are in controlling inflation.</p>
<p>One useful tip is to remember that <em>interest rates take two years to fully work their way through the economy.</em></p>
<p>There is further analysis of the usefulness of the policy you can read about here: <a target="_blank" href="files/Monetary policy will not be enough to fight the next recession _ The Economist.pdf">Monetary policy will not be enough to fight the next recession (The Economist)</a></p>
