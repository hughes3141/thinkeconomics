<h2 id="q01">Britain&rsquo;s Cocaine Glut</h2>
<p>7 December 2017</p>
<p><a href="https://www.economist.com/britain/2017/12/07/britains-cocaine-glut" target="_blank">https://www.economist.com/britain/2017/12/07/britains-cocaine-glut</a></p>

<p>Cocaine in London in 2017 is much cheaper than it was five years ago. Dealers know that their product is no longer scarce, and many are resorting to discounts (e.g. &ldquo;Black Friday discounts&rdquo;) to boost sales.</p>
<p>The quantity of the drug consumed seems to be rising by most estimates. Between 2010 and 2016 the number amount of cocaine seized by the authorities doubled to 5.5 tonnes. Analysis of sewage in London shows that there is double the amount of substances which the human body produces after consuming the drug. The number of crack cocaine users rose by about 10% between 2011 and 2015.</p>
<p>Street prices have not changed, though wholesale prices have fallen by 15% between 2014 and 2016. (Street prices do not change because users already know the standard price per gram).</p>
<p>Why has this been happening? Colombia, a main producer of the drug, has increased its production of coca from 49,000 hectare in 2012 to 146,000 in 2016. The Colombian government also stopped spraying weed killer on coca fields from airplanes. Also, as part of the recent peace deal in Colombia farmers were given money to switch away from coca- but the farmers knew this beforehand, so they grew more coca before the deal was signed to get more money for switching.</p>
<p>Another factor is that demand in America is falling, due to users getting older and tougher crackdowns on trafficking from Mexico.</p>
<p>The cheaper cocaine has a human price- the number of deaths related to cocaine or crack in England and Wales jumped from 139 in 2012 to 371 in 2016.</p>

