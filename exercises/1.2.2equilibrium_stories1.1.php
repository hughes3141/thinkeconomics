<p>Loans for university students are generous: graduates only start to pay once they make over &pound;21,000 per year, and after 30 years the loan is written off. The argument against going back to a system without student fees is that wealthy students, who could have paid anyway, will benefit from general taxation while poorer students will not be able to access courses because universities will not be willing to offer them.</p>
