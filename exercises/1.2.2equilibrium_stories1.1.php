<h2 id="q03" style="clear:both">The perilous politics of parking</h2>
<p>6 April 2017</p>
<p><a href="https://www.economist.com/leaders/2017/04/06/the-perilous-politics-of-parking" target="_blank">https://www.economist.com/leaders/2017/04/06/the-perilous-politics-of-parking</a></p>
<p><a href="files/The Perilous Politics of Parking.pdf" target="_blank">Article PDF</a></p>
<p>Think of parking as an economic good- it is supplied, either by the government or through private individuals who are forced to create parking when they build new buildings, and it is demanded by drivers, who sometimes often have to pay for it. Space given to parking lots certainly has an opportunity cost- just think of the other parks or buildings that could be used on the land instead.</p>
<p>In many places people can park on the street for nothing, or for very little. In Boston, MA, most meters charge $1.25 per hour. Because drivers spend so long looking for these great deals, many end up circling the block. In the German city of Freiburg one study found that 75% of all cars on the road were simply looking for a place to park.</p>
<p>In order to increase parking spaces, many cities require developers to build new parking whenever they build a new building. In St Paul, MN, there must be four spaces for every hole on a golf course. The new Apple HQ in California has 318,000 square metres for office and laboratories; the city of Cupertino has a law that required them to build 325,000 square metres of cark parks.</p>
<p>Cities should stop trying to increase the supply of parking and instead should raise the prices until the streets and car parks are nearly, but not quite, full. This would help speed the transport revolution that will come about from self-driving cars.</p>
