<p><a href="files/Raking it In.pdf" target="_blank">Article PDF</a></p>
<p>The hottest property investment in the UK is where you may least expect it- arable farmland. Over the past decade farmland prices have grown at twice the rate of prime London property prices. Farmland in the UK is 3 time as much as in America, and 15 time as much as farmland in Australia.</p>
<p>One reason is that people just aren&rsquo;t selling it. In 2000 300,000 acres were on the market, but last year it was below 150,000, or less than 0.5% of Britain&rsquo;s farmland. Another reason is that climate change is affecting food supply abroad, which makes British farmland more attractive. Tax breaks on owning farmland help because farmland is exempt from inheritance tax after seven years as long as the farmer farms it.</p>
<p>Foreign investors and pension funds are attracted to farmland because it is seen as a good investment, particularly in a recession. Farmers are growing richer as well, due to rising prices in their crops (wheat was 80% more expensive in 2014 than it was in 2009) and are buying more farmland from golf-course developers. Farmers themselves in 2014 made up 66% of buyers, up from 40% in 2004.</p>
<p>This trend is set to continue, as the supply of farmland shrinks every year. The only thing that makes it look like this growth will slow could be that prices of crops may fall soon. This will be due to the increased number of producers of these crops, which would push down their price.</p>
