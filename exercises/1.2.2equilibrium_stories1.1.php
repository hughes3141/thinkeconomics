<p><a href="files/The French are Fretting Over a Butter Shortage.pdf" target="_blank">Article PDF</a></p>
<p>There was a catastrophe in Paris in 2017: butter prices soared. Bakers have been able to keep their production of croissants (which use lots of butter), but they are making lower profit margins because the cost of butter is so high.</p>
<p>In some places, such as local supermarkets, there are butter shortages. Some consumers are hoarding what they can find. Butter is essential for much French cuisine, and the French eat more of it than anybody else.</p>
<p>Some part of this explanation is that demand is growing from other parts of the world. &ldquo;China has discovered croissants,&rdquo; says Emmanuelle Auriole of the Toulouse School of Economics. Others in western countries are buying more full-cream milk, cheese, and butter as they become more informed of the dangers of alternatives such as sugar.</p>
<p>A global dairy-price index is up by 27% in a year. However, suppliers have responded slowly to the rise in price because recent reforms made it harder for them to plan production.</p>
<p>One solution would be for French cooks to look to alternatives instead of butter. One reporter asked a baker whether she might try to bake with margarine. &ldquo;We use butter or nothing,&rdquo; the baker responded, wagging a finger. Clearly this would be unacceptable.</p>
