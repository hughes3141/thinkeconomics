<h1>Mergers and Acquisitions: The new rules of attraction</h1>
<p>Article written in 2014</p>
<a href="files/152.01.pdf" target="_blank">Link to Article Here</a>
<ol>
<li>What are the signs that mergers and acquisitions (M&amp;A) are &lsquo;back with a vengeance?</li><a href="#" onclick="id01()">Click for answer</a>
</ol>
<p class="ans" id="id01">$3.6trn of deals were announced this year (2014). Some deals are worth more than $10bn. Cross-border deals are 43% of activity.</p>
<ol start="2">
<li>What is the main criticism of M&amp;A, according to &lsquo;veteran fund managers and academics&rsquo;?</li><a href="#" onclick="id02()">Click for answer</a>
</ol>
<p class="ans" id="id02">That the deals satisfy executive vanity an enrich their bankers but destroy value for shareholders.</p>
<ol start="3">
<li>What tends to happen to an acquiring firm&rsquo;s shares when a deal is announced?</li><a href="#" onclick="id03()">Click for answer</a>
</ol>
<p class="ans" id="id03">Share values fall.</p>
<ol start="4">
<li>What are three reasons why M&amp;A make sense &lsquo;on paper&rsquo;?</li><a href="#" onclick="id04()">Click for answer</a>
</ol>
<p class="ans" id="id04">Two firms combine to cut overheads, raising their margins (economies of scale). Adding together market share means they increase their pricing power. Firms can cross-sell each other&rsquo;s products in each other&rsquo;s geographic markets.</p>
<ol start="5">
<li>What are some examples of the best- and worst-ever mergers?</li><a href="#" onclick="id05()">Click for answer</a>
</ol>
<p class="ans" id="id05">Best: Exxon-Mobil, due to under-appreciated assets. AB Inbev doing $100bn of deals over 20 years to increase their profits. Worst: Time Warner and AOL, RBS and ABN AMRO.</p>
<ol start="6">
<li>&ldquo;Most M&amp;A deals create value.&rdquo; What are the figures to back this up?</li><a href="#" onclick="id06()">Click for answer</a>
</ol>
<p class="ans" id="id06">Since 2000, the combined market capitalisations of buyers and target firms has risen when a deal is announced.</p>
<ol start="7">
<li>&ldquo;The problem is . . .&rdquo; Explain why not all shareholders might be happy with this increase in value.</li><a href="#" onclick="id07()">Click for answer</a>
</ol>
<p class="ans" id="id07">Because all of the value that is added goes to the target firm. This suggests that many times the buyer overpays, for reasons such as exaggerating the reasons for pursuing the deal in the first place.</p>
<ol start="8">
<li>Explain how more assertive investors may ensure that M&amp;A deals are more thoughtful this time around.</li><a href="#" onclick="id08()">Click for answer</a>
</ol>
<p class="ans" id="id08">Because if shareholders are more active (&ldquo;militant and engaged&rdquo; as the article puts it), and if institutional shareholders are more important, they might have a greater voice in protecting the shareholder value of their companies than they otherwise would. (Plus the banks that used to fund these big deals may now be more restrained following the financial crisis).</p>
<ol start="9">
<li>Why might &lsquo;bandwagons&rsquo; lead to bad M&amp;A deals?</li><a href="#" onclick="id09()">Click for answer</a>
</ol>
<p class="ans" id="id09">Because if there is a trend in the market then managers might do a deal without thinking it through. There were several such &lsquo;trends&rsquo; mentioned in the past: the monopolies of the 1890s and 1900s; in the 1960s conglomerates; in 1999-2000 the dotcom bubble.</p>
<ol start="10">
<li>The article describes a trend for companies to &lsquo;spin off&rsquo; assets. Why does Altria&rsquo;s split into four main parts suggest this is good for shareholders?</li><a href="#" onclick="id10()">Click for answer</a>
</ol>
<p class="ans" id="id10">Because the four parts, collectively valued at $333bn, is double the net worth of the combined group.</p>

