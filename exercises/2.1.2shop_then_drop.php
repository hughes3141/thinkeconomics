<h1>Shop, then drop:&nbsp;Britain&rsquo;s consumer boom shows signs of petering out</h1>
<h2>The spending spree that has kept the economy going since Brexit may be nearing its end</h2>
<p><a target="_blank" href="https://www.economist.com/britain/2017/02/04/britains-consumer-boom-shows-signs-of-petering-out">https://www.economist.com/britain/2017/02/04/britains-consumer-boom-shows-signs-of-petering-out</a></p>
<p>Feb 4th 2017</p>
<img src="files/2.1.2_05.jpg" alt="1930s Shoppers">
<p>FOR the past seven months the British economy has defied the predictions of analysts, most of whom expected a recession to follow the Brexit referendum in June. GDP grew by 2% in 2016, faster than in any other G7 country&mdash;and the economy did better in the second half than in the first. The unexpectedly strong performance is largely thanks to the efforts of households, which have been spending liberally. In the fourth quarter of 2016 the volume of retail sales, excluding petrol, was 6% higher than in the year before, the biggest rise since 2004. Nor is the weak pound preventing families from splurging on foreign holidays (see&nbsp;<a  target="_blank" href="http://www.economist.com/news/britain/21716041-sterling-may-be-down-foreign-trips-are-up-british-tourists-have-not-let-weak-pound-spoil">article</a>). But there are signs that Britons&rsquo; freewheeling ways may not last much longer.</p>
<p>The fortunes of the economy rise and fall with households&rsquo; spending habits. In the years after the financial crisis of 2008-09, belts were tightened as people lost their jobs and real wages fell. But retail spending picked up from 2014. This was thanks to falling unemployment and faster earnings growth. In 2011-13 real disposable household income shrank on average by 2% a year but in 2014-16 it grew by 3% a year. The median (after taxes and benefits) now stands at &pound;26,300 ($33,300).</p>
<p>With this in mind it is not surprising that consumer spending remained strong after the Brexit vote. The result itself may even have spurred consumption. Half of voters plumped for Leave, after all, so they may be happier shoppers than before. Those who are gloomier about Britain&rsquo;s prospects seem to have brought their consumption forward, loading up on foreign goods before the weak pound causes prices to rise. The value of sales of drinks and tobacco, much of which are imported, jumped by 25% year on year in December.</p>
<p>But in recent months Britons&rsquo; desire for new iPads and sofas has outpaced their ability to pay for them. After the referendum, households continued to indulge in unsecured borrowing, thanks in part to the Bank of England&rsquo;s looser monetary policy. People now appear to have decided that with Brexit negotiations about to get under way and the attendant economic uncertainty, they should focus less on borrowing and more on repaying. On January 31st the Bank of England revealed that consumer-credit growth in December fell to &pound;1bn from &pound;1.9bn the month before.</p>
<p>Rising prices will also cramp consumer spending. Inflation may near 3% by the end of the year. Food staples are getting dearer: those who gave up Marmite on their breakfast toast after it became more expensive last year will soon have nowhere to hide, after Weetabix warned this week that it too was considering a price rise. As the cost of everyday essentials goes up, households will have less money left over for other things, making them feel poorer. Credit Suisse, a bank, reckons that consumer-spending growth will drop from 2.8% last year to 0.7% in 2017.</p>
<p>This is worrying because until now consumers&rsquo; willingness to spend has helped to prop up an otherwise poorly supported economy. The government is tightening austerity this year, sucking away demand. Investment is weak by historical standards and is likely to slip further as firms hold off on plans to expand their operations in Brexit-bound Britain. Bank lending to non-financial firms fell by 1% in the fourth quarter. And despite the sharp drop in the value of sterling since June, exports are hardly booming. If the consumer-spending spree comes to an end too, it is hard to see where economic growth will come from.</p>
<p><em>This article appeared in the Britain section of the print edition under the headline "Britain&rsquo;s consumer boom shows signs of petering out"</em></p>
