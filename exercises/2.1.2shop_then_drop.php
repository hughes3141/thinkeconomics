<li><p>What does GDP measure?</p><button class="toggleAnswer">Click to show answer</button>
<p class="answer">GDP measures economic output in the economy. It is a proxy for income (GDP = Output = Income = Expenditure)</p></li>

<li><p>Why would you expect British families to cut back on foreign holidays due to a weak pound?</p><button class="toggleAnswer">Click to show answer</button>
<p class="answer">A weak pound would make foreign holidays more expensive. For example: if the pound were weak, say &pound;1 = $1.25, then a hotel room in the USA costing $200 per night would cost &pound;160 (200 &divide; 1.25 = 160). If the pound were strong, say &pound;1 = $2, then the same hotel room would cost &pound;100 (200 &divide; 2 = 100). Thus weak pound makes foreign holidays more affordable for British families, suggesting that British families would <em>import</em> more of these foreign experiences.</p></li>

<li><p>Explain, using information in paragraph 2, how unemployment and real disposable income affect consumption.</p><button class="toggleAnswer">Click to show answer</button>
<p class="answer">Higher unemployment will lead to lower consumption because unemployed people have limited income and therefore do not spend as much on consumer goods. Real disposable income growth will allow people to consume more, as is evidenced in the article: retail spending picked up from 2014, at the same time when real disposable income started growing (by 3% a year in 2014-2016, compared to a fall in 2% pear year from 2011-2013).</p></li>

<li><p>Explain the two reasons why consumption may have risen following the Brexit vote, according to paragraph 3.</p><button class="toggleAnswer">Click to show answer</button>
<p class="answer">A majority (over 50%) of people voted for Brexit, so the result made them happy and willing to spend more money. People who were uncertain about the future would have also brought their consumption forward, afraid that prices might go up soon.</p></li>

<li><p>What kind of interest rate does &lsquo;looser monetary policy&rsquo; imply? How will interest rates affect consumption?</p><button class="toggleAnswer">Click to show answer</button>
<p class="answer">&lsquo;Looser monetary policy&rsquo; means that the Bank of England has lowered interest rates. Lower interest rates would encourage consumer spending, because (a) it is cheaper to borrow money that can be used for consumption and (b) those on variable-rate mortgages pay less in mortgage payments each month, and therefore have more money for consumer goods.</p></li>

<li><p>What is the relationship between consumer spending and inflation, according to paragraph 5? Which of the three reasons of AD sloping downwards does this relate to?</p><button class="toggleAnswer">Click to show answer</button>
<p class="answer">As inflation increases, consumer spending will decrease. This refers to the <strong>real balance effect</strong>: given a certain <em>nominal income</em>, inflation will mean that <em>real incomes</em> are lower and consumers cannot afford to buy as much.</p></li>

<li><p>What has government spending been like during this time? What effect will this have on AD?</p><button class="toggleAnswer">Click to show answer</button>
<p class="answer">Government spending has been weak. Government policy was one of <strong>austerity</strong>, meaning that government budgets were lowered to bring down overall spending levels. According to the article, this will &lsquo;suck away demand.&rsquo;</p></li>

<li><p>What has Investment been like during this time? Why has this been the case (the article mentions two reasons why businesses are not investing)?</p><button class="toggleAnswer">Click to show answer</button>
<p class="answer">Investment is weak, not to its usual level. This is because (a) firms, uncertain over future Brexit arrangements, are holding off on their plans to expand and (b) banks are not lending as much to businesses.</p></li>

<li><p>What has been happening to Net Exports? How does this relate to the exchange rate? Is this what we would expect?</p><button class="toggleAnswer">Click to show answer</button>
<p class="answer">Net exports are &lsquo;hardly booming.&rsquo; The exchange rate is weak (&lsquo; . . . sharp drop in the value of sterling since June . . .&rsquo;), which would suggest that net exports should be expanding. We would expect the opposite; a lower exchange rate should mean that net exports increase. (This is a good example of how unresponsive net exports are to changes in exchange rates)</p></li>
</ol>

