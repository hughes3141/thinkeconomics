<h1>Mergers and Acquisitions: Research</h1>
<p>Research a Merger or Takeover of Your Choice. Key information is to include:</p>
<ul>
<li>Cost of Merger</li>
<li>Key Players</li>
<li>What type of merger? (e.g. Horizontal, Vertical, Conglomerate)</li>
<li>What was gained from the merger?</li>
<li>What was the strategy? (e.g. economies of scale, increasing market share, &ldquo;synergies&rdquo;)</li>
</ul>
<p>This is meant to be descriptive. Have fun, find information about a company that you <em>genuinely think is interesting</em>. This should be 1-2 paragraphs MAXIMUM.</p>
<p>One strategy is to use a reputable source (e.g. the Economist) to find an article about the merger, then conduct supplemental research using other sources.</p>
